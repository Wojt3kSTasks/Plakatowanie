    private static int[] getInput(String testName) {
        int[] buildingsHeights = null;
        try {
            URI uri = Thread.currentThread().getContextClassLoader().getResource(testName + ".in").toURI();
            File inFile = new File(uri);
            Scanner scanner = new Scanner(inFile);
            StringTokenizer input = new StringTokenizer(scanner.nextLine());
            int numberOfBuildings = Integer.parseInt(input.nextToken());
            buildingsHeights = new int[numberOfBuildings];
            for (int i = 0; i < numberOfBuildings; i++) {
                input = new StringTokenizer(scanner.nextLine());
                input.nextToken();
                buildingsHeights[i] = Integer.parseInt(input.nextToken());
            }
        } catch (Exception e) {
        }
        return buildingsHeights;
    }

    private static int getOutput(String testName) {
        int numberOrBillboards = 0;
        try {
            URI uri = Thread.currentThread().getContextClassLoader().getResource(testName + ".out").toURI();
            File inFile = new File(uri);
            Scanner scanner = new Scanner(inFile);
            StringTokenizer input = new StringTokenizer(scanner.nextLine());
            numberOrBillboards = Integer.parseInt(input.nextToken());
        } catch (Exception e) {
        }
        return numberOrBillboards;
    }
